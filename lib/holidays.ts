const HOLIDAYS: Record<number, HolidayMap> = {
  2026: H_2026,
  2027: H_2027,
}

/** 回傳該日的假日名稱，若非國定假日則回傳 null */
export function getHolidayName(year: number, month: number, day: number): string | null {
  return HOLIDAYS[year]?.[`${month}-${day}`] ?? null
}

/** 該年份是否已收錄官方假日資料 */
export function hasHolidayData(year: number): boolean {
  return year in HOLIDAYS
}
